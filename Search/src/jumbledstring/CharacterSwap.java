package jumbledstring;

public class CharacterSwap {
    private final int indexA, indexB;

    public CharacterSwap(int indexA, int indexB) {
        this.indexA = indexA;
        this.indexB = indexB;
    }

    public int getIndexA() {
        return indexA;
    }

    public int getIndexB() {
        return indexB;
    }
}
